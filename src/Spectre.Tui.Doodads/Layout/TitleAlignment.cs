namespace Spectre.Tui.Doodads.Layout;

/// <summary>
/// Specifies how a border title is aligned within the top border line.
/// </summary>
public enum TitleAlignment
{
    /// <summary>
    /// Title is aligned to the left, after the top-left corner.
    /// </summary>
    Left,

    /// <summary>
    /// Title is centered within the top border line.
    /// </summary>
    Center,

    /// <summary>
    /// Title is aligned to the right, before the top-right corner.
    /// </summary>
    Right,
}
